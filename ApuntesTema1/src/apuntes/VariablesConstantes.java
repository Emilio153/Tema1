package apuntes;

public class VariablesConstantes {

	public static void main(String[] args) {
		
		//Para comentar un bloque seleccionamos con el ratón y pulsamos Ctrl+7
		
		/* Esto es un comentario multilínea
		 * 
		 * En este ejercicio:
		 * - Declaro una variable
		 * - Declaro una constante
		 * - Muestro las dos
		 */
		
		//Estoy declarando la variable a
		int a = 1;
		
		//Estoy declarando una constante
		final double NUMERO_PI = 3.1416;
		
		//Aquí estoy mostrando la variable a y la constante NUMERO_PI
		System.out.println("La variable a vale " + a + "y el número pi es " + NUMERO_PI);	

	}

}
